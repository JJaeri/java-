package chapter2;

public class CharacterEx1 {

	public static void main(String[] args) {

		char ch1 = 'Z';
		System.out.print(ch1);			// 문자출력
		System.out.print((int)ch1);		// 문자에 해당하는 정수 값 출력
		
		char ch2 = 38;					// 정수값 대입
		System.out.print(ch2);			// 정수 값에 해당하는 문자 출력
		
		int ch3 = 97;					
		System.out.print(ch3);			// 문자 정수 값 출력
		System.out.print((char)ch3);	// 정수 값에 해당하는 문자 출력

	}

}
